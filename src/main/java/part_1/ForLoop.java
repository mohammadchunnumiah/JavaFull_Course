package part_1;

public class ForLoop {

	public static void main(String[] args) {

		// initialization
		// condition
		// increment / decrement

		// increment
		for (int i = 0; i < 5; i++)
			System.out.println(i);
		
		System.out.println("------------------------------");

		// decrement
		for (int j = 10; j > 0; j--) {
			System.out.println(j);
		}
	}
}
